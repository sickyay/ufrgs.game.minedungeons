int draw_map (char *map[MAP_HEIGHT][MAP_WIDTH]) {

    BeginDrawing();
    ClearBackground(RAYWHITE);//Limpa a tela e define cor de fundo

    for (int y = 0; y < MAP_HEIGHT; y++) {
            for (int x = 0; x < MAP_WIDTH; x++) {
                    switch(*map[y][x]){

                    case '#':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, BLACK);
                        break;
                    case 'B':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, GRAY);
                        break;
                    case 'P':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, PINK);
                        break;
                    case 'J':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, GREEN);
                        break;
                    case 'X':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, BROWN);
                        break;
                    }
            }
    }
    EndDrawing();
    return 1;
}
int deveMover(struct struct_status a){

    a.x = a.x+a.dx;
    a.y = a.y+a.dy;

    if(a.x>=0&&a.x<(LARGURA/LADO)&&a.y>=0&&a.y<(ALTURA/LADO)){
        return 1;
    }else{
        return 0;
    }
}

void move(struct struct_status *a){

    a->x = a->x+a->dx;
    a->y = a->y+a->dy;
}

