struct struct_status{
    int x;
    int y;
    int dx;
    int dy;
    int flag;
    int count;
    int health_points;
};

int draw_map (char matrix[MAP_HEIGHT][MAP_WIDTH],struct struct_status a) {

    BeginDrawing();
    ClearBackground(RAYWHITE);//Limpa a tela e define cor de fundo

    for (int y = 0; y < MAP_HEIGHT; y++) {
            for (int x = 0; x < MAP_WIDTH; x++) {
                    switch(matrix[y][x]){

                    case '#':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, BLACK);
                        break;
                    case 'I':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, RED);
                        break;
                    case 'B':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, GRAY);
                        break;
                    case 'P':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, PURPLE);
                        break;
                    case 'J':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, GREEN);
                        break;
                    case 'X':
                        DrawRectangle(x*LADO, y*LADO, LADO, LADO, BROWN);
                        break;
                    case 'o':
                        DrawRectangle((x*LADO)+5,(y*LADO)+5, LADO/2, LADO/2, BLUE);
                        break;
                    }
            }
    }
    DrawText(TextFormat("Ammo: %03i", a.count), 20, 2, 40, RED);
    EndDrawing();
    return 1;
}
int deveMover(struct struct_status a, char matrix[MAP_HEIGHT][MAP_WIDTH]){

    a.x = a.x+a.dx;
    a.y = a.y+a.dy;

    if(matrix[a.y][a.x]==' '){
        if(a.x>=0&&a.x<MAP_WIDTH&&a.y>=0&&a.y<MAP_HEIGHT){
            return 1;
        }else{
            return 0;
        }
    }else{
        return 0;
    }
}

void move(struct struct_status *a,char matrix[MAP_HEIGHT][MAP_WIDTH]){

    char temp;

    temp = matrix[a->y][a->x];
    matrix[a->y][a->x] = ' ';

    a->x = a->x + a->dx;
    a->y = a->y + a->dy;

    matrix[a->y][a->x] = temp;
}

int shoot (struct struct_status *a,struct struct_status *b, char matrix[MAP_HEIGHT][MAP_WIDTH],int c,int d){

    b->x = a->x;
    b->y = a->y;

    b->dx = c;
    b->dy = d;

    if(deveMover(*b,matrix)){
        matrix[(a->y)+d][(a->x)+c] = 'o';
        a->count = a->count + 1;
    }
}

